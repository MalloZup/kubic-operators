#!/bin/sh

while [ $# -gt 0 ] ; do
  case $1 in
    --package|-p)
      PACKAGE=$2
      shift
      ;;
    --in|-i)
      IN=$2
      shift
      ;;
    --out|-o)
      OUT=$2
      shift
      ;;
    --var|-v)
      VAR=$2
      shift
      ;;
    *)
      echo "Unknown argument $1"
      exit 1
      ;;
  esac
  shift
done

abort() { echo >&2 "!!! FATAL: $@. Aborting." ; exit 1 ; }

[ -n "$IN"      ] || abort "no input file provided with --in"
[ -f $IN        ] || abort "input file $IN does not exist"
[ -n "$PACKAGE" ] || abort "no package provided with --package"
[ -n "$VAR"     ] || abort "no variable provided with --var"

if [ -n "$OUT" ] ; then
    OUT=$(dirname $IN)/$OUT.generated.go
else
    BIN=$(basename $IN)
    OUT=$(dirname $IN)/$(echo "$BIN" | tr '[:upper:]' '[:lower:]').generated.go
fi

echo "Generating $OUT from $IN..."

cat<<EOF > $OUT
